void setup()
{
    Serial.begin(9600);

    // SSD1306_SWITCHCAPVCC = generate display voltage from 3.3V internally
    if (!display.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS))
    {
        Serial.println(F("SSD1306 allocation failed"));
        for (;;)
            ; // Don't proceed, loop forever
    }
    Serial.println(F("SSD1306 allocation good"));

    // Show initial display buffer contents on the screen --
    // the library initializes this with an Adafruit splash screen.
    display.display();
    delay(2000); // Pause for 2 seconds

    // Clear the buffer
    display.clearDisplay();

    display.drawBitmap(
        (display.width() - WIDTH) / 2,
        16,
        git_logo, WIDTH, HEIGHT, 1);

    display.setTextSize(1);              // Normal 1:1 pixel scale
    display.setTextColor(SSD1306_WHITE); // Draw white text
    display.cp437(true);                 // Use full 256 char 'Code Page 437' font
    display.setCursor(0, 0);             // Start at top-left corner

    display.print("GIT");

    display.display();
    delay(3000);

    display.clearDisplay();

    display.drawBitmap(
        (display.width() - WIDTH) / 2,
        16,
        msvs_logo, WIDTH, HEIGHT, 1);
    display.setCursor(0, 0); // Start at top-left corner

    display.print("Microsoft\nVisual Studio");
    display.display();
    delay(3000);
    display.clearDisplay();

    display.drawBitmap(
        (display.width() - WIDTH) / 2,
        16,
        vscode_logo, WIDTH, HEIGHT, 1);
    display.setCursor(0, 0); // Start at top-left corner

    display.print("Visual Studio Code");
    display.display();
    delay(3000);
}